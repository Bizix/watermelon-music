create policy playlist_songs_select_via_own_playlist
    on public.playlist_songs
    for select
    to authenticated
    using (
        exists (
            select 1
            from public.playlists
            where playlists.id = playlist_songs.playlist_id
              and playlists.user_id = auth.uid()
        )
    );

